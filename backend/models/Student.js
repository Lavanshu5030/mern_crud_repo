const { DataTypes } = require('sequelize');
const sequelize = require('../database/connection'); // Import your Sequelize instance

const Student = sequelize.define('Student', {
  username: {
    type: DataTypes.STRING,
    allowNull: false,
  },
  email: {
    type: DataTypes.STRING,
    allowNull: false,
  },
  password: {
    type: DataTypes.STRING,
    allowNull: false,
  },
  image: {
    type: DataTypes.STRING,
    allowNull: false,
  },
});

module.exports = Student;






